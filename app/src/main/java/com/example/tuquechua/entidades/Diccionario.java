package com.example.tuquechua.entidades;

public class Diccionario {
    private String dicPalabraQuechua;
    private String dicSignificado;
    private int idDic;
    private String dicPalabraEspanol;

    public String getDicPalabraQuechua() {
        return dicPalabraQuechua;
    }

    public void setDicPalabraQuechua(String dicPalabraQuechua) {
        this.dicPalabraQuechua = dicPalabraQuechua;
    }

    public String getDicSignificado() {
        return dicSignificado;
    }

    public void setDicSignificado(String dicSignificado) {
        this.dicSignificado = dicSignificado;
    }

    public int getIdDic() {
        return idDic;
    }

    public void setIdDic(int idDic) {
        this.idDic = idDic;
    }

    public String getDicPalabraEspanol() {
        return dicPalabraEspanol;
    }

    public void setDicPalabraEspanol(String dicPalabraEspanol) {
        this.dicPalabraEspanol = dicPalabraEspanol;
    }
}
